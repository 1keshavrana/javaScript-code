let siz = prompt("choose size of popcorn box : \n1. XL \n2. L \n3. M \n4. S");

if ( siz == "XL" || siz == "xl"){
    alert(`Price : ${250}`);
}
else if ( siz == "L" || siz == "l"){
    alert(`Price : ${200}`);
}
else if ( siz == "M" || siz == "m"){
    alert(`Price : ${100}`);
}
else if ( siz == "S" || siz == "s"){
    alert(`Price : ${50}`);
}